
class Calculator: # создали класс Calculator
   def multiply(self, x, y): # метод класса: умножение
       return x * y

   def division(self, x, y): # метод класса: деление
       return x / y

   def subtraction(self, x, y): #метод класса: вычитание
       return x - y

   def adding(self, x, y): # метод класса: сложение
       return x + y